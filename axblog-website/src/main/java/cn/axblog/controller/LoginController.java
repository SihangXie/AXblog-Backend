package cn.axblog.controller;

import cn.axblog.common.JSONResult;
import cn.axblog.domain.entity.User;
import cn.axblog.domain.vo.WebsiteUserLoginVo;
import cn.axblog.service.LoginService;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.web.bind.annotation.PostMapping;
import org.springframework.web.bind.annotation.RequestBody;
import org.springframework.web.bind.annotation.RestController;

@RestController
public class LoginController {

    @Autowired
    private LoginService loginService;

    @PostMapping("/login")
    public JSONResult<WebsiteUserLoginVo> login(@RequestBody User user) {
        return JSONResult.success(loginService.login(user));
    }
}
